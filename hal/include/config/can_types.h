/************************************************************************
 * HAL层 - CAN类型定义
 *
 * 定义CAN硬件接口使用的基础数据类型
 ************************************************************************/

#ifndef CAN_TYPES_H
#define CAN_TYPES_H

#include "osa_types.h"

/*
 * ============================================================================
 * CAN帧结构（硬件层）
 * ============================================================================
 */

typedef struct
{
    uint32     can_id;      /* CAN标识符 */
    uint8      dlc;         /* 数据长度 (0-8) */
    uint8      data[8];     /* 数据字节 */
    uint32     timestamp;   /* 时间戳 (可选) */
} can_frame_t;

#endif /* CAN_TYPES_H */
